package com.apps.skygreen.pesonapurworejo.model;

public class RecomendedWisataModel {
    private String idWisata;
    private String namaWisata;
    private String alamat;
    private String icon;
    private String Kategori;
    private String jenisWisata;
    private String tiketMasuk;
    private String deskripsi;

    public String getJenisWisata() {
        return jenisWisata;
    }

    public void setJenisWisata(String jenisWisata) {
        this.jenisWisata = jenisWisata;
    }

    public String getTiketMasuk() {
        return tiketMasuk;
    }

    public void setTiketMasuk(String tiketMasuk) {
        this.tiketMasuk = tiketMasuk;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getIdWisata() {
        return idWisata;
    }

    public void setIdWisata(String idWisata) {
        this.idWisata = idWisata;
    }

    public String getNamaWisata() {
        return namaWisata;
    }

    public void setNamaWisata(String namaWisata) {
        this.namaWisata = namaWisata;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getKategori() {
        return Kategori;
    }

    public void setKategori(String kategori) {
        Kategori = kategori;
    }
}
