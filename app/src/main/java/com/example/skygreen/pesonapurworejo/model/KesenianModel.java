package com.example.skygreen.pesonapurworejo.model;

public class KesenianModel {
    private String idKesenian;
    private String namaKesenian;
    private String kategori;
    private String icon;
    private String deskripsi;
    private String pengelola;
    private String sosialMedia;
    private String alamat;

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getPengelola() {
        return pengelola;
    }

    public void setPengelola(String pengelola) {
        this.pengelola = pengelola;
    }

    public String getSosialMedia() {
        return sosialMedia;
    }

    public void setSosialMedia(String sosialMedia) {
        this.sosialMedia = sosialMedia;
    }

    public String getIdKesenian() {
        return idKesenian;
    }

    public void setIdKesenian(String idKesenian) {
        this.idKesenian = idKesenian;
    }

    public String getNamaKesenian() {
        return namaKesenian;
    }

    public void setNamaKesenian(String namaKesenian) {
        this.namaKesenian = namaKesenian;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }
}
